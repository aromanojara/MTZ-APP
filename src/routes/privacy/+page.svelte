		<h3>Módulos disponibles</h3>

		<h4>1.- Asistencia a Clases</h4>
		<p>El módulo de asistencia a clases facilita el seguimiento de la participación de los jugadores en las sesiones de entrenamiento regulares. Los miembros del equipo pueden marcar su asistencia a cada sesión para mantener un registro claro de su dedicación y progreso.</p>

		<h4>2.- Asistencia a Partidos Oficiales</h4>
		<p>Con el módulo de asistencia a partidos oficiales, los jugadores pueden confirmar su disponibilidad para los encuentros competitivos. Al registrar su asistencia el equipo y el entrenador podrán planificar de manera efectiva la alineación y estrategias para cada partido.</p>

		<h4>3.- Asistencia a Eventos</h4>
		<p>Este módulo permite al equipo coordinar su presencia en eventos especiales, como team buildings, "mesticarretes" o reuniones importantes relacionadas con el equipo. Los miembros pueden confirmar su asistencia a estos eventos cruciales y estar mejor preparados para realizar cálculos respecto a cuotas o aforos.</p>

		<h4>4.- Pagos</h4>
		<p>El módulo de pagos simplifica el proceso de comunicar y verificar los pagos de los jugadores para diversas actividades. Aunque no se generan transacciones dentro de la aplicación, se proporciona una función para que los miembros puedan indicar que han realizado sus pagos manualmente, asegurando una gestión financiera clara y transparente.</p>

